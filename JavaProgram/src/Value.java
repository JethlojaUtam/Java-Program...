/*
Program 01
Create a Java program that declares variables for various data types(int, double, 
char, boolean) and initializes them with values. Display these values on the console.
*/
public class Value 
{
    public static void main(String[] args) 
    {
      
        int a = 10;
        double b = 10.5;
        char c = 'A';
        boolean d = true;

        System.out.println("Integer: " + a);
        System.out.println("Double: " + b);
        System.out.println("Character: " + c);
        System.out.println("Boolean: " + d);
    }
}

/*run:
Integer: 10
Double: 10.5
Character: A
Boolean: true
*/